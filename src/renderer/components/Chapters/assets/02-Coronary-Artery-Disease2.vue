                        in these relatives.<sup>4,7,8,9</sup></p>
                    <h2 id="other-risk-factors">Other Risk Factors</h2>
                    <p>A number of other factors are known to be associated with the risk to develop CAD. The likelihood of developing CAD increases with age. The risk of CAD is higher in African American populations than Caucasian populations and lower in the Hispanic
