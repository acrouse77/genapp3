                    <h2 id="inheritance-and-family-history">Inheritance and Family History</h2>
                    <p>CAD is a multifactorial condition, meaning that both genetic and environmental factors combine to affect risk to develop the disease. Heritability is a term used to estimate the amount that genetic factors contribute to the difference in disease risk
