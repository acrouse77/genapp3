                <h1 class="text-primary">Description</h1>

                    <p>Age-related macular degeneration (AMD) is an eye disease characterized by loss of central vision due to changes in the macular region of the retina.<sup>1</sup> In the United States, AMD has an estimated prevalence of 1 in 2,000 individuals.
                        The prevalence increases with age, affecting more than 11.5% of the population over age 80.<sup>2</sup> AMD is the leading cause of blindness in developed countries.<sup>3</sup> Quality of life can be greatly impacted by AMD, as central vision loss
